            "                'num_sentences': len(sentences),\n",
            "                'summary_sentences': summary_length,\n",
            "                'compression_ratio': max(0, (1 - len(summary.split()) / max(1, len(text.split()))) * 100)\n",
            "            }\n",
            "            \n",
            "            return summary, metrics\n",
